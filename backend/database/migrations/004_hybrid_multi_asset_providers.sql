-- Create audit table if not exists, matching providers.id type
DO $$
DECLARE
  v_id_type TEXT;
BEGIN
  -- Detect providers.id column type (UUID or BIGINT)
  SELECT data_type INTO v_id_type
  FROM information_schema.columns
  WHERE table_schema = 'public'
    AND table_name = 'providers'
    AND column_name = 'id';

  -- Create audit table with matching ID type
  IF v_id_type = 'uuid' THEN
    CREATE TABLE IF NOT EXISTS public.provider_audit (
      id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
      provider_id UUID REFERENCES public.providers(id) ON DELETE CASCADE,
      provider_code TEXT NOT NULL,
      asset_class TEXT,
      action TEXT NOT NULL CHECK (action IN ('created', 'updated', 'activated', 'deactivated', 'deleted', 'health_check')),
      changed_fields JSONB DEFAULT '{}'::jsonb,
      old_values JSONB DEFAULT '{}'::jsonb,
      new_values JSONB DEFAULT '{}'::jsonb,
      changed_by UUID REFERENCES auth.users(id),
      changed_by_ip INET,
      reason TEXT,
      created_at TIMESTAMPTZ DEFAULT NOW() NOT NULL
    );
    RAISE NOTICE 'Created provider_audit with UUID id type';
  ELSE
    CREATE TABLE IF NOT EXISTS public.provider_audit (
      id BIGSERIAL PRIMARY KEY,
      provider_id BIGINT REFERENCES public.providers(id) ON DELETE CASCADE,
      provider_code TEXT NOT NULL,
      asset_class TEXT,
      action TEXT NOT NULL CHECK (action IN ('created', 'updated', 'activated', 'deactivated', 'deleted', 'health_check')),
      changed_fields JSONB DEFAULT '{}'::jsonb,
      old_values JSONB DEFAULT '{}'::jsonb,
      new_values JSONB DEFAULT '{}'::jsonb,
      changed_by UUID REFERENCES auth.users(id),
      changed_by_ip INET,
      reason TEXT,
      created_at TIMESTAMPTZ DEFAULT NOW() NOT NULL
    );
    RAISE NOTICE 'Created provider_audit with BIGINT id type';
  END IF;
END$$;

