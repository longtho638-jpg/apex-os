-- =====================================================
-- 5. CREATE TRIGGER: Auto-audit
-- =====================================================

DROP FUNCTION IF EXISTS public.audit_provider_changes CASCADE;

CREATE OR REPLACE FUNCTION public.audit_provider_changes()
RETURNS TRIGGER AS $$
BEGIN
  IF (TG_OP = 'INSERT') THEN
    INSERT INTO public.provider_audit (
      provider_id, provider_code, asset_class, action, new_values, changed_by
    ) VALUES (
      NEW.id, NEW.provider_code, NEW.asset_class, 'created', 
      to_jsonb(NEW) - 'encrypted_api_key' - 'encrypted_api_secret' - 'encrypted_credentials',
      NEW.created_by
    );
    RETURN NEW;
    
  ELSIF (TG_OP = 'UPDATE') THEN
    INSERT INTO public.provider_audit (
      provider_id, provider_code, asset_class, action, 
      changed_fields, old_values, new_values, changed_by
    ) VALUES (
      NEW.id, NEW.provider_code, NEW.asset_class, 'updated',
      (SELECT jsonb_object_agg(key, value) 
       FROM jsonb_each(to_jsonb(NEW)) 
       WHERE key NOT IN ('encrypted_api_key', 'encrypted_api_secret', 'encrypted_credentials', 'updated_at')
       AND to_jsonb(OLD)->>key IS DISTINCT FROM to_jsonb(NEW)->>key),
      to_jsonb(OLD) - 'encrypted_api_key' - 'encrypted_api_secret' - 'encrypted_credentials',
      to_jsonb(NEW) - 'encrypted_api_key' - 'encrypted_api_secret' - 'encrypted_credentials',
      NEW.updated_by
    );
    RETURN NEW;
    
  ELSIF (TG_OP = 'DELETE') THEN
    INSERT INTO public.provider_audit (
      provider_id, provider_code, asset_class, action, old_values
    ) VALUES (
      OLD.id, OLD.provider_code, OLD.asset_class, 'deleted',
      to_jsonb(OLD) - 'encrypted_api_key' - 'encrypted_api_secret' - 'encrypted_credentials'
    );
    RETURN OLD;
  END IF;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

DROP TRIGGER IF EXISTS trigger_audit_providers ON public.providers;
CREATE TRIGGER trigger_audit_providers
  AFTER INSERT OR UPDATE OR DELETE ON public.providers
  FOR EACH ROW EXECUTE FUNCTION public.audit_provider_changes();

